package br.com.fuelclub.entity;

public class Usuario {
	
	
	public String usuario_nome;
	public double usuario_telefone;
	public String usuario_cpf;
	public String usuario_email;
	public String usuario_senha;
	
	
	//get e set
	
	
	public String getUsuario_nome() {
		return usuario_nome;
	}
	public void setUsuario_nome(String usuario_nome) {
		this.usuario_nome = usuario_nome;
	}
	public double getUsuario_telefone() {
		return usuario_telefone;
	}
	public void setUsuario_telefone(double usuario_telefone) {
		this.usuario_telefone = usuario_telefone;
	}
	public String getUsuario_cpf() {
		return usuario_cpf;
	}
	public void setUsuario_cpf(String usuario_cpf) {
		this.usuario_cpf = usuario_cpf;
	}
	public String getUsuario_email() {
		return usuario_email;
	}
	public void setUsuario_email(String usuario_email) {
		this.usuario_email = usuario_email;
	}
	public String getUsuario_senha() {
		return usuario_senha;
	}
	public void setUsuario_senha(String usuario_senha) {
		this.usuario_senha = usuario_senha;
	}
	
	

}
