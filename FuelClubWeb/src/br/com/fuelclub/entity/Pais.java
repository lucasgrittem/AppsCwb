package br.com.fuelclub.entity;

public class Pais {
	
	
	public String pais_descricao;
	public String pais_sigla;
	
	
	//get e set
	
	
	public String getPais_descricao() {
		return pais_descricao;
	}
	public void setPais_descricao(String pais_descricao) {
		this.pais_descricao = pais_descricao;
	}
	public String getPais_sigla() {
		return pais_sigla;
	}
	public void setPais_sigla(String pais_sigla) {
		this.pais_sigla = pais_sigla;
	}
	
	

}
